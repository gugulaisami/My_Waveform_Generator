void Chinese_Show_one(u8 x, u8 y, u8 num, u8 size, u8 mode)
{
    u8 temp,t,t1;
    u8 y0=y;
    u8 csize=(size/8 + ((size%8)?1:0)) * size;     //     16/8+1
    for(t=0;t<csize;t++)
    {  
        if(size==16)     
					temp = chinese[num][t];     
        else 
					return;      
        for(t1=0;t1<8;t1++)
        {
            if(temp&0x80) 
							LCD_Fast_DrawPoint(x,y,POINT_COLOR);
            else 
							LCD_Fast_DrawPoint(x,y,BACK_COLOR);
            temp<<=1;
            y++;
            if((y-y0)==size)
            {
                y=y0;
                x++;
                break;
            } 
         }  
     }  
}




